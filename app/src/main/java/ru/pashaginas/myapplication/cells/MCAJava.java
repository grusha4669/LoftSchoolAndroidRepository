package ru.pashaginas.myapplication.cells;

class MCAJava {
   private String title;
   private String value;

   public MCAJava(String title, String value) {
      this.title = title;
      this.value = value;
   }

   public String getTitle() {
      return title;
   }

   public String getValue() {
      return value;
   }
}